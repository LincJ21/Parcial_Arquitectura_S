class Benefit:
    """
    Clase que representa un beneficio.
    """
    def __init__(self, name, rewards):
        """
        Inicializa un beneficio con su nombre y recompensas asociadas.

        :param name: Nombre del beneficio.
        :param rewards: Recompensas asociadas al beneficio.
        """
        self.name = name
        self.rewards = rewards
